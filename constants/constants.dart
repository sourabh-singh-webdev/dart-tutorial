void main() {
  /// In dart we can use two keywords to make our variable constants :
  /// 1). using the "final" keyword: -- It does not preserve any memory until it is not accessed (run time value can be assigned)
  /// 2). using the "const" keyword: -- It preserve memory (compile time value is assigned)
  ///
  final name = 'sourabh';
  // can not be reassigned
  const age = '20';
}
